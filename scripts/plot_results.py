#!/usr/bin/env python3
"""Render P2 paper figures + summary table from ``results/p2_main/`` JSON.

Reads the suite/ablation JSON written by ``run_main_eval.py`` and produces, under
``paper/figures/``:

  * fig_generalization.pdf        — in-distribution vs. held-out solve rate + gap.
  * fig_perturbation.pdf          — solve rate vs. perturbation magnitude per split.
  * fig_length_extrapolation.pdf  — solve rate vs. system length (#variables).
  * summary_table.md              — headline numbers + ablation status, paper-ready.

Figures are vector PDFs at paper scale. Missing inputs are skipped with a warning
rather than crashing, so a partial run still emits whatever it can.

Usage:
    python scripts/plot_results.py
    python scripts/plot_results.py --results results/p2_main --figures paper/figures
"""

from __future__ import annotations

import argparse
import json
from pathlib import Path
from typing import Any

import matplotlib

matplotlib.use("Agg")
import matplotlib.pyplot as plt

ID_COLOR = "#2c3e50"
HO_COLOR = "#e67e22"


def _load(path: Path) -> dict | None:
    if not path.exists():
        print(f"  ! missing {path} — skipping")
        return None
    return json.loads(path.read_text())


# --------------------------------------------------------------------------- figs

def plot_generalization(data: dict, out: Path) -> None:
    idm = data["splits"]["in_distribution"]
    hom = data["splits"]["held_out_structure"]
    gap = data["generalization_gap"]

    fig, ax = plt.subplots(figsize=(4.2, 3.6))
    labels = ["in-distribution\n(2-var)", "held-out structure\n(3-var)"]
    rates = [idm["solve_rate"], hom["solve_rate"]]
    bars = ax.bar(labels, rates, color=[ID_COLOR, HO_COLOR], width=0.6)
    for b, r in zip(bars, rates):
        ax.text(b.get_x() + b.get_width() / 2, r + 0.02, f"{r:.2f}",
                ha="center", fontweight="bold")
    ax.set_ylabel("solve rate (pass@1)")
    ax.set_ylim(0, 1.08)
    ax.set_title(f"Generalization gap = {gap:.3f}\nsolver: {data.get('solver', '?')}")
    # annotate the gap as a bracket between the two bars
    ax.annotate(
        "", xy=(1, hom["solve_rate"]), xytext=(1, idm["solve_rate"]),
        arrowprops=dict(arrowstyle="<->", color="#c0392b", lw=1.4),
    )
    fig.tight_layout()
    fig.savefig(out, format="pdf")
    plt.close(fig)
    print(f"  wrote {out}")


def plot_perturbation(data: dict, out: Path) -> None:
    deltas = [row["delta"] for row in data["sweep"]]
    id_rate = [row["in_distribution"]["perturbed_solve_rate"] for row in data["sweep"]]
    ho_rate = [row["held_out_structure"]["perturbed_solve_rate"] for row in data["sweep"]]

    fig, ax = plt.subplots(figsize=(4.6, 3.6))
    ax.plot(deltas, id_rate, "o-", color=ID_COLOR, label="in-distribution")
    ax.plot(deltas, ho_rate, "s-", color=HO_COLOR, label="held-out structure")
    ax.set_xlabel("perturbation magnitude  Δ (constant shift)")
    ax.set_ylabel("solve rate after re-deriving")
    ax.set_ylim(0, 1.08)
    ax.set_title(f"Perturbation robustness\nsolver: {data.get('solver', '?')}")
    ax.legend(frameon=False)
    ax.grid(True, alpha=0.25)
    fig.tight_layout()
    fig.savefig(out, format="pdf")
    plt.close(fig)
    print(f"  wrote {out}")


def plot_length(data: dict, out: Path) -> None:
    lengths = [row["length"] for row in data["sweep"]]
    solve = [row["solve_rate"] for row in data["sweep"]]
    passk = [row["pass_at_k"] for row in data["sweep"]]
    train_len = data.get("train_length", 2)

    fig, ax = plt.subplots(figsize=(4.6, 3.6))
    ax.plot(lengths, solve, "o-", color=ID_COLOR, label="solve rate (pass@1)")
    ax.plot(lengths, passk, "^--", color=HO_COLOR, label=f"pass@{data.get('k', 'k')}")
    ax.axvline(train_len + 0.5, color="#7f8c8d", ls=":", lw=1.2)
    ax.text(train_len + 0.6, 0.05, "train ≤ here", color="#7f8c8d", fontsize=8)
    ax.set_xlabel("system length  (# variables)")
    ax.set_ylabel("solve rate")
    ax.set_ylim(0, 1.08)
    ax.set_title(f"Length extrapolation\nsolver: {data.get('solver', '?')}")
    ax.legend(frameon=False)
    ax.grid(True, alpha=0.25)
    fig.tight_layout()
    fig.savefig(out, format="pdf")
    plt.close(fig)
    print(f"  wrote {out}")


# ------------------------------------------------------------------- summary table

def _fmt(x: Any, nd: int = 3) -> str:
    return f"{x:.{nd}f}" if isinstance(x, (int, float)) else str(x)


def build_summary_table(
    gen: dict | None,
    pert: dict | None,
    length: dict | None,
    noise: dict | None,
    guid: dict | None,
    pur: dict | None,
) -> str:
    lines: list[str] = ["# P2 — Main Paper Results\n"]
    solver = (gen or {}).get("solver", "?")
    lines.append(f"_Solver: **{solver}**. Generated by `scripts/plot_results.py`._\n")

    # headline suites
    lines.append("## Headline metrics\n")
    lines.append("| Suite | Metric | Value |")
    lines.append("|---|---|---|")
    if gen:
        idm = gen["splits"]["in_distribution"]
        hom = gen["splits"]["held_out_structure"]
        lines.append(f"| Generalization | in-distribution solve rate | {_fmt(idm['solve_rate'])} |")
        lines.append(f"| Generalization | held-out solve rate | {_fmt(hom['solve_rate'])} |")
        lines.append(f"| Generalization | **generalization gap** | **{_fmt(gen['generalization_gap'])}** |")
    if pert:
        worst = max(pert["sweep"], key=lambda r: r["delta"])
        lines.append(
            f"| Perturbation | solve rate at Δ={_fmt(worst['delta'],2)} (held-out) | "
            f"{_fmt(worst['held_out_structure']['perturbed_solve_rate'])} |"
        )
    if length:
        train_len = length.get("train_length", 2)
        extrap = [r for r in length["sweep"] if r["length"] > train_len]
        if extrap:
            longest = max(extrap, key=lambda r: r["length"])
            lines.append(
                f"| Length extrap. | solve rate at length {longest['length']} | "
                f"{_fmt(longest['solve_rate'])} |"
            )
    lines.append("")

    # ablations
    lines.append("## Ablations\n")
    lines.append("| Ablation | Status | Key result |")
    lines.append("|---|---|---|")
    if noise:
        red = noise.get("entrapment_reduction_mean")
        ci = noise.get("entrapment_reduction_ci95", 0.0)
        verdict = "noise reduces entrapment" if noise.get("noise_helps") else "⚠ noise did not help"
        lines.append(f"| Noise on/off | ok | reduction {_fmt(red)} ± {_fmt(ci)} ({verdict}) |")
    if guid:
        if guid.get("status") == "ok":
            best_rate = max((r["solve_rate"] for r in guid.get("sweep", [])), default=0.0)
            lines.append(
                f"| Guidance weight | ok | best w = {guid.get('best_weight')} "
                f"(solve rate {_fmt(best_rate)}, checkpoint: `{Path(guid.get('checkpoint', '')).name}`) |"
            )
        else:
            lines.append(f"| Guidance weight | skipped | {guid.get('reason')} |")
    if pur:
        if pur.get("status") == "ok":
            gain = pur.get("shaping_gain_overall_solve_rate")
            std_rate = pur["standard"]["overall_solve_rate"]
            pur_rate = pur["purist"]["overall_solve_rate"]
            lines.append(
                f"| Purist reward | ok | shaping gain {_fmt(gain)} "
                f"(standard {_fmt(std_rate)} vs. purist {_fmt(pur_rate)} overall solve rate) |"
            )
        else:
            lines.append(f"| Purist reward | skipped | {pur.get('reason')} |")
    lines.append("")

    if (guid and guid.get("status") == "ok") or (pur and pur.get("status") == "ok"):
        lines.append(
            "> **Checkpoint-scale caveat:** the guidance/purist checkpoints above are "
            "toy-scale Stage-A/Stage-B runs (`scripts/train_p2_checkpoints.py`, D=128, "
            "L=4, CPU, minutes not GPU-hours) trained to unblock the eval pipeline end "
            "to end, not to demonstrate solve capability — see "
            "`results/p4_scale/scaling_notes.md` and `results/p4_scale/roadmap.md` for "
            "the full-scale training plan. Headline suite numbers above use the "
            "deterministic `refine` baseline, which is unaffected by checkpoint scale.\n"
        )

    lines.append("## Figures\n")
    lines.append("- `fig_generalization.pdf`")
    lines.append("- `fig_perturbation.pdf`")
    lines.append("- `fig_length_extrapolation.pdf`")
    lines.append("")
    return "\n".join(lines)


def main() -> None:
    parser = argparse.ArgumentParser(description="Render P2 figures + summary table")
    parser.add_argument("--results", default="results/p2_main")
    parser.add_argument("--figures", default="paper/figures")
    args = parser.parse_args()

    res = Path(args.results)
    figs = Path(args.figures)
    figs.mkdir(parents=True, exist_ok=True)

    print(f"Reading results from {res}/")
    gen = _load(res / "generalization.json")
    pert = _load(res / "perturbation.json")
    length = _load(res / "length_extrapolation.json")
    noise = _load(res / "ablation_noise.json")
    guid = _load(res / "ablation_guidance.json")
    pur = _load(res / "ablation_purist.json")

    print(f"Writing figures to {figs}/")
    if gen:
        plot_generalization(gen, figs / "fig_generalization.pdf")
    if pert:
        plot_perturbation(pert, figs / "fig_perturbation.pdf")
    if length:
        plot_length(length, figs / "fig_length_extrapolation.pdf")

    table = build_summary_table(gen, pert, length, noise, guid, pur)
    table_path = figs / "summary_table.md"
    table_path.write_text(table)
    print(f"  wrote {table_path}")
    print("== done ==")


if __name__ == "__main__":
    main()
